      <p>Name</p>
      <p>Description</p>

      <p><code>{ATTRIBUTES.id}</code></p>
      <p>
        The <code>id</code> to use for this element in <code>toc</code> context. If not provided,
        this will be the element <code>id</code>, or generated by <code>toc</code> if element does
        not have an <code>id</code> either<br />
        <span class="inline-block pt-2 text-sm">
          <strong>type</strong>: <code>string</code><br />
          <strong>mutability</strong>: customizable
        </span>
      </p>

      <p><code>{ATTRIBUTES.ignore}</code></p>
      <p>
        Whether to ignore this element when searching for matching elements<br />
        <span class="inline-block pt-2 text-sm">
          <strong>type</strong>: <code>boolean</code><br />
          <strong>mutability</strong>: customizable
        </span>
      </p>

      <p><code>{ATTRIBUTES.strategy}</code></p>
      <p>
        Override the <code>strategy</code> for this element to use in creating
        <code>IntersectionObserver</code>. This only has effect if the <code>observe</code> option
        is enabled in <code>toc</code> parameters<br />
        <span class="inline-block pt-2 text-sm">
          <strong>type</strong>: <code>'parent' | 'self' | 'auto'</code><br />
          <strong>mutability</strong>: customizable
        </span>
      </p>

      <p><code>{ATTRIBUTES.threshold}</code></p>
      <p>
        Override the <code>threshold</code> for this element to use in creating
        <code>IntersectionObserver</code>. This only has effect if the <code>observe</code> option
        is enabled in <code>toc</code> parameters<br />
        <span class="inline-block pt-2 text-sm">
          <strong>type</strong>: <code>number</code><br />
          <strong>mutability</strong>: customizable
